-- 11. Are repeat buyers also more likely to subscribe?

-- 11. Are repeat buyers also more likely to subscribe?
SELECT
    CASE 
        WHEN previous_purchases > 5 THEN 'Repeat Buyers'
        ELSE 'Normal Buyers'
    END AS customer_type,
    subscription_status,
    COUNT(*) AS customer_count,
    ROUND(
        (COUNT(*) * 100.0 / SUM(COUNT(*)) OVER (
            PARTITION BY CASE WHEN previous_purchases > 5 THEN 'Repeat Buyers' ELSE 'Normal Buyers' END
        )), 2
    ) AS customer_percentage
FROM customer_sales
GROUP BY 1, 2
ORDER BY customer_type, customer_count DESC;

-- Business Problem :-
-- Relationship between repeat buying and subscriptions is unclear.

-- Impact :-
-- Improves subscription targeting
-- Supports loyalty strategies
-- Increases customer retention

