-- 10. Top 3 most purchased products within each category

WITH top_products AS (
    SELECT 
        category,
        item_purchased,
        COUNT(*) AS total_purchases,
        DENSE_RANK() OVER (
            PARTITION BY category
            ORDER BY COUNT(*) DESC
        ) AS ranks
    FROM customer_sales
    GROUP BY category, item_purchased
)

SELECT 
    category,
    item_purchased,
    total_purchases,
    ranks
FROM top_products
WHERE ranks <= 3
ORDER BY category, ranks, total_purchases DESC;

-- Business Problem :-
-- Company does not know top-performing products within categories.

-- Impact :-
-- Improves inventory optimization
-- Supports recommendation systems
-- Increases sales through best-selling products

