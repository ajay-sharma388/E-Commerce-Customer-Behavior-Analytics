-- 9. Segment customers into New, Returning, and Loyal customers

WITH customer_segments AS (
    SELECT 
        CASE 
            WHEN previous_purchases = 0 THEN 'New Customer'
            WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer'
            ELSE 'Loyal Customer'
        END AS customer_segment
    FROM customer_sales
)
SELECT 
    customer_segment,
    COUNT(*) AS customer_count
FROM customer_segments
GROUP BY customer_segment
ORDER BY customer_count DESC;

-- Business Problem :-
-- Lack of customer segmentation results in generic marketing strategies.

-- Impact :-
-- Enables personalized campaigns
-- Improves retention strategies
-- Increases customer engagement

