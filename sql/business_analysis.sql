-- 6. Compare average purchase between Standard and Express shipping

SELECT 
    shipping_type,
    COUNT(*) AS total_orders,
    ROUND(AVG(purchase_amount), 2) AS avg_purchase,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM customer_sales
WHERE shipping_type IN ('Standard', 'Express')
GROUP BY shipping_type
ORDER BY total_revenue DESC;

-- Business Problem :-
-- Company does not know if faster shipping influences spending.

-- Impact :-
-- Optimizes shipping strategies
-- Encourages premium shipping usage
-- Increases average order value

-- 7. Do subscribed customers spend more?

SELECT 
    subscription_status,
    COUNT(*) AS total_orders,
    ROUND(AVG(purchase_amount), 2) AS avg_spend,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM customer_sales
GROUP BY subscription_status
ORDER BY total_revenue DESC;

-- Business Problem :-
-- Subscription program effectiveness is unclear.

-- Impact :-
-- Evaluates loyalty programs
-- Improves customer retention
-- Increases customer lifetime value

-- 8. Top 5 products with the highest discount usage percentage

SELECT 
    item_purchased,
    SUM(CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END) AS discount_count,
    ROUND(
        SUM(CASE WHEN discount_applied = 'Yes' THEN 1 ELSE 0 END) * 100.0
        / COUNT(*), 2
    ) AS discount_usage_pct
FROM customer_sales
GROUP BY item_purchased
ORDER BY discount_usage_pct DESC
LIMIT 5;

-- Business Problem :-
-- Some products may be overly dependent on discounts.

-- Impact :-
-- Helps optimize pricing strategy
-- Reduces profit margin loss
-- Identifies discount-driven products

