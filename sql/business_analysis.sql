-- 1. Which category generates the highest revenue?

SELECT 
    category,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM customer_sales
GROUP BY category
ORDER BY total_revenue DESC;

-- Business Problem :-
-- Company does not know which category contributes the most revenue.

-- Impact :-
-- Helps prioritize high-performing categories
-- Optimizes inventory planning
-- Improves marketing ROI

-- =====================================================

-- 2. Are discounts increasing purchase value?

SELECT
    discount_applied,
    ROUND(SUM(purchase_amount), 2) AS total_revenue,
    ROUND(AVG(purchase_amount), 2) AS avg_purchase
FROM customer_sales
GROUP BY discount_applied
ORDER BY total_revenue DESC;

-- Business Problem :-
-- Discounts may reduce profits without increasing sales value.

-- Impact :-
-- Measures discount effectiveness
-- Helps reduce unnecessary discounts
-- Improves pricing strategy

-- 3. What is the total revenue generated by male and female customers?

SELECT 
    gender,
    ROUND(SUM(purchase_amount), 2) AS total_revenue
FROM customer_sales
GROUP BY gender
ORDER BY total_revenue DESC;

-- Business Problem :-
-- Lack of visibility into revenue contribution by gender segments.

-- Impact :-
-- Supports targeted marketing campaigns
-- Improves customer segmentation
-- Enhances personalization efforts

-- =====================================================

-- 4. Which customers used discounts but still spent more than the average purchase amount?

SELECT 
    customer_id,
    purchase_amount
FROM customer_sales
WHERE discount_applied = 'Yes'
AND purchase_amount > (
    SELECT AVG(purchase_amount)
    FROM customer_sales
);

-- Business Problem :-
-- Company cannot identify high-spending discount-sensitive customers.

-- Impact :-
-- Helps target premium customers
-- Improves retention campaigns
-- Supports strategic discount offers

-- 5. Top 5 products with the highest average review ratings

SELECT 
    item_purchased,
    ROUND(AVG(review_rating), 2) AS avg_ratings
